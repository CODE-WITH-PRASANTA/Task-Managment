const mongoose = require('mongoose');

const VIP3TaskSchema = new mongoose.Schema({
  fileUrl: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('VIP3Task', VIP3TaskSchema);
